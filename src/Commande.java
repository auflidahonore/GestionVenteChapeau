    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        try{
            model.setRowCount(0);
            {
                if( jComboBox1.getSelectedIndex() ==0){
                    Rs=ps.executeQuery("Select * from commande WHERE numCmd='"+txtrecherche.getText()+"'");
                }
                else if( jComboBox1.getSelectedIndex() ==1){
                    Rs=ps.executeQuery("Select * from commande WHERE idFrns='"+txtrecherche.getText()+"'");
                }

                else if( jComboBox1.getSelectedIndex() ==2){
                    Rs=ps.executeQuery("Select * from commande WHERE idChap='"+txtrecherche.getText()+"'");
                }
               
                else if( jComboBox1.getSelectedIndex() ==3){
                    Rs=ps.executeQuery("Select * from commande WHERE quantiteCmd='"+txtrecherche.getText()+"'");
                }
                else if( jComboBox1.getSelectedIndex() ==4){
                    Rs=ps.executeQuery("Select * from commande WHERE dateCmd='"+txtrecherche.getText()+"'");
                }

            }while(Rs.next()){
                Object[]commande={Rs.getInt(1),Rs.getInt(2),
                    Rs.getInt(3),Rs.getInt(4),Rs.getString(5) };
                model.addRow(commande);
            }if(model.getRowCount()==0){JOptionPane.showMessageDialog(null, " il n'y a aucun commande"); }
            else{
                int i=0;
                deplace (i);
            }

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_jButton5ActionPerformed

