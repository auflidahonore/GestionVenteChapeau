     private void deplace(int i){
        try{
            txtnum.setText(model.getValueAt(i,0).toString());
            txtid.setText(model.getValueAt(i,1).toString());
            txtidchap.setText(model.getValueAt(i,2).toString());
            txtquantite.setText(model.getValueAt(i,3).toString());
            txtdate.setText(model.getValueAt(i,4).toString());
        }catch(Exception e){System.err.println(e);
        JOptionPane.showMessageDialog(null,"erreur de deplacement"+e.getLocalizedMessage());}
    }
    
    
      private void afficher(){
         try{
             model.setRowCount(0);
            ps=connect.obtenirConnection().createStatement();
            ResultSet Rs=ps.executeQuery("Select * from commande");
            while(Rs.next()){
            model.addRow(new Object[]{
                 Rs.getInt("numCmd"),Rs.getInt("idFrns"),Rs.getInt("idChap"),
                Rs.getInt("quantiteCmd"),Rs.getString("dateCmd")
            });
            
            }
        }catch(Exception e){
            System.err.println(e);
        }
        tableCmd.setModel(model);
        
    }
    
